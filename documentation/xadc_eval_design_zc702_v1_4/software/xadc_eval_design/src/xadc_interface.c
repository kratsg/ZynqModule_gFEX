int setupSysMon(void) {
    int status;
    XSysMon_Config *sysmonConfig;

    sysmonConfig = XSysMon_LookupConfig(XPAR_SYSMON_0_DEVICE_ID);
    if(sysmonConfig == NULL) {
        return XST_FAILURE;
    }

    XSysMon_CfgInitialize(&sysmon, sysmonConfig, sysmonConfig->BaseAddress);

    /* Set up single-channel operation */
    XSysMon_SetSequencerMode(&sysmon, XSM_SEQ_MODE_SINGCHAN);

    /* Enable channel Vp/Vn */
    status = XSysMon_SetSeqChEnables(&sysmon, XSM_SEQ_CH_VPVN);
    if(status != XST_SUCCESS) {
        xil_printf("ERROR: Unable to set sequencer channel Vp/Vn: 0x%08X\r\n", status);
        return status;
    }

    /* Change the clock divider to slow down the XADC
     * Since we're running at 100 MHz we'll divide by four to bring it down to 25 MHz
     */
    XSysMon_SetAdcClkDivisor(&sysmon, 4);

    /* Enter into continuous channel mode */
    XSysMon_SetSequencerMode(&sysmon, XSM_SEQ_MODE_CONTINPASS);

    return XST_SUCCESS;
}

